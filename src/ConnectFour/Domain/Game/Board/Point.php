<?php

declare(strict_types=1);

namespace Gaming\ConnectFour\Domain\Game\Board;

final class Point
{
    private int $x;

    private int $y;

    public function __construct(int $x, int $y)
    {
        $this->x = $x;
        $this->y = $y;
    }

    public function x(): int
    {
        return $this->x;
    }

    public function y(): int
    {
        return $this->y;
    }
}
